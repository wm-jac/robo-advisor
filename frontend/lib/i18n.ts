export function t(lang: Lang, key: string, params?: Record<string, string | number>): string {
  const dict = TRANSLATIONS[lang] as Record<string, unknown>;
  const fallback = TRANSLATIONS.en as Record<string, unknown>;
  const template = (dict[key] ?? fallback[key] ?? key) as string;
  return template.replace(/\{(\w+)\}/g, (_, token) => String(params?.[token] ?? `{${token}}`));
}

export function localizeQuestions<T extends { text: string; options: string[] }>(questions: T[], lang: Lang): T[] {
  const translated = TRANSLATIONS[lang].questions;
  return questions.map((question, index) => ({
    ...question,
    text: translated[index]?.text ?? question.text,
    options: translated[index]?.options ?? question.options,
  }));
}

export function translateProfile<T extends { label: string; description: string }>(profile: T, lang: Lang): T {
  if (lang === "en") {
    return profile;
  }
  const key = PROFILE_KEY_MAP[profile.label];
  if (!key) {
    return profile;
  }
  return {
    ...profile,
    label: TRANSLATIONS.zh[`profile_${key}_label` as never] as string,
    description: TRANSLATIONS.zh[`profile_${key}_desc` as never] as string,
  };
}
